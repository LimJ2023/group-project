package chapter01;

public class BinaryTest1 {

	public static void main(String[] args) {
		
		//자료구조 - Type
		
		//정수형 자료타입
		byte aVar = 100;
		short bVar = 30000;
		int cVar = 50000000;
		long dVar = 10000000;
		
		System.out.println("byte타입: " + aVar);
		System.out.println("short타입: " + bVar);
		System.out.println("int타입: " + cVar);
		System.out.println("long타입: " + dVar);
		
		
	}

}
