package chapter01;

public class CharacterEx3 {

	public static void main(String[] args) {
		
		//대문자 A는 65, 소문자는 97
		int a = 65;
		System.out.println(a);
		System.out.println((char)a);
		
		System.out.println("===============");
		
		int b = 66;
		System.out.println(b);
		System.out.println((char)b);
		
		System.out.println("===============");
		
		int a2 = 97;
		System.out.println(a2);
		System.out.println((char)a2);
	}

}
