package chapter01;

public class BinaryTest3 {

	public static void main(String[] args) {

		//보수법 : 2진법 수를 반대로 한 뒤 +1을 하면 보수가 된다.
		
		int num1 = 0B00000000000000000000000000000101; //2진수 5
		int num2 = 0B11111111111111111111111111111010; //2진수 -6
		int num3 = 0B11111111111111111111111111111011;
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		
		
	}

}
